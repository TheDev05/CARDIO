//**C++ Program To Display Size Of Different Datatype

#include <iostream>

int main()
{
    std::cout << "SIZE OF INTEGERS: " << sizeof(int) << std::endl;
    std::cout << "SIZE OF FLOATS: " << sizeof(float) << std::endl;
    std::cout << "SIZE OF CHARACTERS: " << sizeof(char) << std::endl;

    return (0);
}